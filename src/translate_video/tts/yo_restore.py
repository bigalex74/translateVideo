# ── Скомпилированные паттерны ────────────────────────────────────────────────

def _build_patterns() -> list[tuple[re.Pattern, str]]:
    """Построить список (pattern, replacement) с учётом регистра."""
    patterns = []
    for wrong, correct in _YO_DICT.items():
        if wrong == correct:
            continue  # пропускаем «нет замены»
        # Компилируем с учётом регистра: слово, Слово, СЛОВО
        esc = re.escape(wrong)
        esc_cap = re.escape(wrong.capitalize())
        esc_up  = re.escape(wrong.upper())
        # Один паттерн с тремя вариантами
        pat = re.compile(
            rf'\b(?:{esc_up}|{esc_cap}|{esc})\b',
            re.UNICODE,
        )
        # Соответствующие замены (в порядке приоритета через функцию)
        cap = correct.capitalize()
        up  = correct.upper()
        wrong_up  = wrong.upper()
        wrong_cap = wrong.capitalize()

        def make_repl(w, c, wc, wu, cc, cu):
            def repl(m: re.Match) -> str:
                s = m.group(0)
                if s == wu:  return cu
                if s == wc:  return cc
                return c
            return repl

        patterns.append((pat, make_repl(wrong, correct, wrong_cap, wrong_up, cap, up)))
    return patterns


_PATTERNS: list[tuple[re.Pattern, callable]] | None = None


def _get_patterns() -> list[tuple[re.Pattern, callable]]:
    global _PATTERNS
    if _PATTERNS is None:
        _PATTERNS = _build_patterns()
    return _PATTERNS


def restore_yo(text: str) -> str:
    """Восстановить «ё» в тексте по словарному методу.

    Применяет замены е→ё для ~200 наиболее частотных слов.
    Безопасна: слова из словаря «нет ё» пропускаются без изменений.

    Args:
        text: Исходный текст (переведённый, до расстановки ударений).

    Returns:
        Текст с восстановленным «ё».
    """
    if not text:
        return text
    for pat, repl in _get_patterns():
        text = pat.sub(repl, text)
    return text
