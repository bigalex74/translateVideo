"""Нормализация текста перед синтезом речи (Yandex SpeechKit).

Преобразует спецсимволы, числа и аббревиатуры в форму, которую TTS
правильно произносит:

  24/7         → двадцать четыре на семь
  5/5          → пять из пяти
  №5           → номер пять
  100%         → сто процентов
  $100 / €50   → сто долларов / пятьдесят евро
  API          → А Пэ И  (или оставить — SpeechKit сам читает)
  ИИ           → И И     (убираем двойные кириллические, чтобы ruaccent не падал)
  ...          → …       (троеточие)
  +7 (999)...  → телефонные номера

Порядок применения правил важен: сначала специфические, потом общие.
"""
from __future__ import annotations

import re

from translate_video.tts.yo_restore import restore_yo

# ── Числительные ──────────────────────────────────────────────────────────────

_ONES_M = {
    0: '', 1: 'один', 2: 'два', 3: 'три', 4: 'четыре',
    5: 'пять', 6: 'шесть', 7: 'семь', 8: 'восемь', 9: 'девять',
    10: 'десять', 11: 'одиннадцать', 12: 'двенадцать', 13: 'тринадцать',
    14: 'четырнадцать', 15: 'пятнадцать', 16: 'шестнадцать',
    17: 'семнадцать', 18: 'восемнадцать', 19: 'девятнадцать',
}
_TENS = {
    2: 'двадцать', 3: 'тридцать', 4: 'сорок', 5: 'пятьдесят',
    6: 'шестьдесят', 7: 'семьдесят', 8: 'восемьдесят', 9: 'девяносто',
}
_HUNDREDS = {
    1: 'сто', 2: 'двести', 3: 'триста', 4: 'четыреста',
    5: 'пятьсот', 6: 'шестьсот', 7: 'семьсот', 8: 'восемьсот', 9: 'девятьсот',
}


def _int_to_words(n: int) -> str:
    """Целое число (0–999) → слова (мужской род, именительный падеж)."""
    if n == 0:
        return 'ноль'
    parts = []
    if n >= 100:
        parts.append(_HUNDREDS[n // 100])
        n %= 100
    if n >= 20:
        parts.append(_TENS[n // 10])
        n %= 10
    if n in _ONES_M:
        w = _ONES_M[n]
        if w:
            parts.append(w)
    return ' '.join(parts)


_ONES_GEN = {
    0: 'нуля', 1: 'одного', 2: 'двух', 3: 'трёх', 4: 'четырёх',
    5: 'пяти', 6: 'шести', 7: 'семи', 8: 'восьми', 9: 'девяти',
    10: 'десяти', 11: 'одиннадцати', 12: 'двенадцати', 13: 'тринадцати',
    14: 'четырнадцати', 15: 'пятнадцати', 16: 'шестнадцати',
    17: 'семнадцати', 18: 'восемнадцати', 19: 'девятнадцати',
    20: 'двадцати',
}


def _int_to_words_gen(n: int) -> str:
    """Целое число (0–20) → слова в родительном падеже (для «из N»)."""
    return _ONES_GEN.get(n, _int_to_words(n))


# ── Словарь замен ─────────────────────────────────────────────────────────────

# Порядок важен: более специфичные — раньше
_REPLACEMENTS: list[tuple[re.Pattern, str | callable]] = [

    # ── Символы валют ──────────────────────────────────────────────────────
    # $100 → сто долларов
    (re.compile(r'\$\s*(\d+(?:[.,]\d+)?)'), lambda m: f'{_money(m.group(1))} долларов'),
    (re.compile(r'€\s*(\d+(?:[.,]\d+)?)'), lambda m: f'{_money(m.group(1))} евро'),
    (re.compile(r'£\s*(\d+(?:[.,]\d+)?)'), lambda m: f'{_money(m.group(1))} фунтов'),
    # 100$ → сто долларов
    (re.compile(r'(\d+(?:[.,]\d+)?)\s*\$'), lambda m: f'{_money(m.group(1))} долларов'),
    (re.compile(r'(\d+(?:[.,]\d+)?)\s*€'), lambda m: f'{_money(m.group(1))} евро'),
    (re.compile(r'(\d+(?:[.,]\d+)?)\s*£'), lambda m: f'{_money(m.group(1))} фунтов'),

    # ── Дроби и спецсочетания с / ──────────────────────────────────────────
    # 24/7 → двадцать четыре на семь
    (re.compile(r'\b(\d{1,3})/7\b'), lambda m: f'{_int_to_words(int(m.group(1)))} на семь'),
    # 5/5, 10/10 → пять из пяти
    (re.compile(r'\b(\d{1,2})/(\d{1,2})\b'),
        lambda m: f'{_int_to_words(int(m.group(1)))} из {_int_to_words_gen(int(m.group(2)))}'),

    # ── Проценты ───────────────────────────────────────────────────────────
    (re.compile(r'(\d+(?:[.,]\d+)?)\s*%'),
        lambda m: f'{_money(m.group(1))} процентов'),

    # ── Номера ─────────────────────────────────────────────────────────────
    (re.compile(r'№\s*(\d+)'), lambda m: f'номер {m.group(1)}'),

    # ── Время ──────────────────────────────────────────────────────────────
    # 14:30 → четырнадцать тридцать
    (re.compile(r'\b(\d{1,2}):(\d{2})\b'),
        lambda m: f'{_int_to_words(int(m.group(1)))} {_int_to_words(int(m.group(2)))}'),

    # ── Аббревиатуры (перед ruaccent они вызывают ошибки) ──────────────────
    # ИИ → И И  (разбиваем кириллические аббревиатуры пробелами)
    (re.compile(r'\b([А-ЯЁ]{2,6})\b'),
        lambda m: ' '.join(m.group(1)) if _is_abbrev(m.group(1)) else m.group(0)),

    # ── Знаки препинания и спецсимволы ─────────────────────────────────────
    # ... → пауза (оставляем как есть — SpeechKit понимает)
    (re.compile(r'\.{3}'), '…'),
    # / оставшийся → пробел (e.g. frontend/backend → frontend backend)
    (re.compile(r'(?<=[а-яёА-ЯЁa-zA-Z])/(?=[а-яёА-ЯЁa-zA-Z])'), ' '),
    # - между словами → пауза
    # (оставляем дефис в словах типа API-ключ, но убираем одиночное тире)
    (re.compile(r'\s+[-–—]\s+'), ', '),
]

# Слова из кириллических заглавных которые точно аббревиатуры (а не названия)
_KNOWN_ABBREVS = {
    'ИИ', 'ИТ', 'БД', 'ПО', 'РФ', 'США', 'СМИ', 'ТВ', 'PR', 'HR',
    'МВД', 'ФСБ', 'ФНС', 'ЦБ', 'РБ', 'ООН', 'ЕС', 'НДС', 'ВВП',
}


def _is_abbrev(s: str) -> bool:
    """Проверить что строка — аббревиатура (не имя, не нарицательное)."""
    return s in _KNOWN_ABBREVS or (len(s) <= 3 and s.isupper())


def _money(s: str) -> str:
    """Число-строка → слова (для сумм, используем целое)."""
    try:
        n = int(float(s.replace(',', '.')))
        return _int_to_words(n)
    except ValueError:
        return s


def normalize(text: str) -> str:
    """Нормализовать текст перед TTS/ruaccent.

    Применяет замены по очереди. Не трогает кириллический текст без
    спецсимволов — такой текст проходит без изменений.

    Args:
        text: Исходный текст (уже переведённый на русский).

    Returns:
        Нормализованный текст, готовый к расстановке ударений и синтезу.
    """
    if not text:
        return text

    # Восстановление «ё» перед всеми остальными заменами — ruaccent работает лучше с ё
    text = restore_yo(text)

    for pattern, replacement in _REPLACEMENTS:
        if callable(replacement):
            text = pattern.sub(replacement, text)
        else:
            text = pattern.sub(replacement, text)

    # Схлопываем множественные пробелы
    text = re.sub(r' {2,}', ' ', text).strip()
    return text
